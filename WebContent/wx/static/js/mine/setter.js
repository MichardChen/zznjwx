$(function(){
	mui('.mui-table-view').on('tap','.set-password',function(){
		mui.openWindow({
			url:'./set_password.html',
			id:'set_password.html'
		})
	})
	mui('.mui-table-view').on('tap','.feedback',function(){
		mui.openWindow({
			url:'./feedback.html',
			id:'feedback.html'
		})
	})
	mui('.mui-content').on('tap','.excit-btn',function(){
		$.ajax({
            url:REQUEST_URL+"wxnonAuthRest/checkout",
            type:"get",
            dataType:"json",
            async:true,
            success:function(data){
                if(data.code == REQUEST_OK){
                    //mui.toast(data.message); 
                    delAllCookie();
                    localStorage.openId="";
                    setTimeout(function(){
                    	/* mui.openWindow({
							url:'../../../index.html',
							id:'index.html'
						})*/
                    	mui.openWindow({
                    		url:"../../../pages/login/login.html",
                    		id:"login.html",
                    		show:{
                		      autoShow:true,//页面loaded事件发生后自动显示，默认为true
                		      aniShow:"slide-in-down",//页面显示动画，默认为”slide-in-right“；
                		      duration:100//页面动画持续时间，Android平台默认100毫秒，iOS平台默认200毫秒；
                		    }
                        })
                    },500)                 
                }else{
                    mui.toast(data.message);
                }               
            }
        })
	})
})
